package Ch03;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("AB" + '\b' + 'C'); // 백슬래쉬
        System.out.println("AB" + '\t' + 'C'); // 공백
        System.out.println("AB" + '\n' + 'C'); // 개행
        System.out.println("AB" + '\r' + 'C');
        
    }
}
